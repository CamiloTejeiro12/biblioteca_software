package com.biblioteca.libro;

/**
 *
 * @author tejei
 */
public class Editorial implements EditorialInterface {

    private String id;
    private String nombre;

    public Editorial(String id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    @Override
    public String getId() {
        return id;
    }

    @Override
    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String getNombre() {
        return nombre;
    }

    @Override
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
